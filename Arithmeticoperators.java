package operators;

public class Arithmeticoperators {

	public static void main(String[] args) {
		
		int a=30;
		int b=20;
		
		System.out.println("Arithmetic operations");
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		System.out.println("Relational operations");
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a<=b);
		System.out.println(a>=b);
	}
}
